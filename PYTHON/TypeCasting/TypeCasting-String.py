# Typecasting on String Datatype
#----->String(int) - int
num='123'
b=int(num)
print(b)

#----->String(int) - Float
b=float(num)
print(b)

#----->String(int) - Bool
b=bool(num)
print(b)

#----->String(int) - Str
b=str(num)
print(b)

#----->String(int) - Complex
b=complex(num)
print(b)

